package com.zot.xing.dao.employ;

public class EmployBO {
	private String employId;
	private String employName;
	private String superEmployId;
	private String employType;
	private String phoneno;
	public String getEmployId() {
		return employId;
	}
	public void setEmployId(String employId) {
		this.employId = employId;
	}
	public String getEmployName() {
		return employName;
	}
	public void setEmployName(String employName) {
		this.employName = employName;
	}
	public String getSuperEmployId() {
		return superEmployId;
	}
	public void setSuperEmployId(String superEmployId) {
		this.superEmployId = superEmployId;
	}
	public String getEmployType() {
		return employType;
	}
	public void setEmployType(String employType) {
		this.employType = employType;
	}
	public String getPhoneno() {
		return phoneno;
	}
	public void setPhoneno(String phoneno) {
		this.phoneno = phoneno;
	}
}
