package com.zot.manage.cost.dao;

public class Cost {

	private String costId;
	
	private String costType;
	
	private String costSubType;
	
	private String remark;
	
	private float costAmount;
	
	private String costOperator;
	
	private String costTime;
	
	private String createTime;
	
	private String costCarNo;
	
	private float costBalance;

	public String getCostId() {
		return costId;
	}

	public void setCostId(String costId) {
		this.costId = costId;
	}

	public String getCostType() {
		return costType;
	}

	public void setCostType(String costType) {
		this.costType = costType;
	}

	public String getCostSubType() {
		return costSubType;
	}

	public void setCostSubType(String costSubType) {
		this.costSubType = costSubType;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public float getCostAmount() {
		return costAmount;
	}

	public void setCostAmount(float costAmount) {
		this.costAmount = costAmount;
	}

	public String getCostOperator() {
		return costOperator;
	}

	public void setCostOperator(String costOperator) {
		this.costOperator = costOperator;
	}

	public String getCostTime() {
		return costTime;
	}

	public void setCostTime(String costTime) {
		this.costTime = costTime;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getCostCarNo() {
		return costCarNo;
	}

	public void setCostCarNo(String costCarNo) {
		this.costCarNo = costCarNo;
	}

	public float getCostBalance() {
		return costBalance;
	}

	public void setCostBalance(float costBalance) {
		this.costBalance = costBalance;
	}
	
	
}
